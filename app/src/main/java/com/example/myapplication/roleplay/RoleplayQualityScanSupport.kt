package com.example.myapplication.roleplay

/**
 * 长文小说模式专用的质量扫描提示词构建器。
 * 包含筋骨扫描、叙事之眼、禁词扫描、行文标点四大类增量规则，
 * 与 [RoleplayAntiClicheSupport] 互补，不重复已有规则。
 */
internal object RoleplayQualityScanSupport {
    fun buildPromptSection(): String {
        return QUALITY_SCAN_PROMPT.trimIndent()
    }

    private val QUALITY_SCAN_PROMPT = """
【长文质量扫描】

A. 筋骨扫描：
1. 所有情绪和态度都通过动作和行为呈现，不要用旁白贴标签。
2. 每句话都包含一个正在发生的事实，不写空洞的情绪概括。
3. 物品和环境描写只写客观物理特征，不加判断性修饰词。
4. 禁止给声音和语气贴标签，不要写"语气里带着……""声音里透着……""语调变得……""嗓音有些……""用一种……的口吻说"。
5. 禁止否定式罗列和对比式矫情，不要写"没有……也没有……而是……""不想……也不想……""不是……而是……""并非……而是……""与其说……不如说……"。直接写做了什么，不写没做什么。
6. 禁止直接照搬设定中的句子，设定是参考而非台词。

B. 叙事之眼：
1. 镜头不俯视或仰视任何角色，保持平视。
2. 旁白不替读者下结论或做评价。
3. 叙述语言不通过对比制造阶级落差。
4. 描写身份、财富、地位、穿着、环境时用词中性，不暗示高低优劣。
5. 不重复上一段已有的描写和动作，每一段都写新的东西。

C. 禁词扫描（以下词汇和句式一律禁用）：
- 平淡、平稳、平静、淡漠、淡淡
- "像陈述事实一样""陈述着"
- 爹味上位者词：审视、不容置喙、不容拒绝、确认主权
- 伪医学解剖词：肋骨、胸腔、气管、视网膜、神经末梢
- 精确物理量化：两厘米、185身高、15度、不到一厘米、迟到一秒钟
- 极端程度副词：极其、极为、异常、非常、十分、特别、超级、极度、无比
- 身体部位和物品前的无功能修饰词（如"纤细的手指""修长的睫毛"等无信息量的废形容词）

D. 行文标点：
1. 连续动作逗号连接，省略重复代词。
2. 不连续两句用相同代词开头（他、她、你等）。
3. 每段句号不超过 1 个。
4. 不使用破折号。
5. 不使用模糊指代（"那人""对方""对面的人"），直接用名字或具体称呼。"""
}
