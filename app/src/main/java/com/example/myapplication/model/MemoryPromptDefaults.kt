package com.example.myapplication.model

/**
 * 长记忆提取 / 注入两段默认提示词。
 *
 * 用户在"记忆与摘要"页留空 → 走这里的默认；用户填写 → 走用户自定义模板（同一渲染路径）。
 * UI 也会把这两段常量作为「载入默认」按钮的内容，方便用户基于默认模板增删改。
 *
 * 占位符约定：
 * - 提取模板：{{conversation}} {{known_memories}} {{user}} {{char}}
 * - 注入模板：{{memories}} {{user}} {{char}}
 */
object MemoryPromptDefaults {
    /**
     * 长记忆提取默认模板。融合 Tavo 风格：信息提取专家 + 分类范围 + 已知信息处理 + 输出示例。
     *
     * 解析侧已支持 JSON 数组 / 无序列表 fallback（参见 MemoryProposalPromptService 解析路径），
     * 因此模板要求"无序列表 + - 开头"也能正常落库。
     */
    val EXTRACTION_PROMPT_TEMPLATE: String = """
        你是信息提取专家，负责从下面的对话中识别并提取适合长期保存的内容：
        1. 与{{user}}相关的关键个人信息（仅限重要、稳定信息）
        2. {{user}}与{{char}}之间发生的重要事件、约定与关系变化

        # 提取重点
        - 关键信息：仅提取{{user}}的重要信息，忽略日常琐事
        - 重要事件：记忆深刻的互动、约定，需包含人物、时间、地点（如有）

        # 提取范围
        - 个人：年龄、生日、职业、学历、居住地
        - 偏好：明确表达的喜好或厌恶
        - 健康：身体状况、过敏史、饮食禁忌
        - 事件：与{{char}}的重要互动、约定
        - 关系：家人、朋友、重要同事
        - 价值观：表达的信念或长期目标
        - 忽略寒暄、一次性任务、临时情绪、重复信息，以及任何思考过程内容

        {{known_memories}}

        # 输出规范
        - 无序列表格式（"- "开头）
        - 每行一条简体中文短句，不要换行
        - 无新信息时返回空白
        - 不要输出任何格式标签（如 markdown、html、JSON 等）

        # 输出示例
        - {{user}}今年22岁，出生于2003年5月20日。
        - {{user}}患有轻度乳糖不耐症，不能饮用纯牛奶。
        - {{user}}与{{char}}在2023年12月25日参加了京都烟火晚会。

        # 待分析对话
        {{conversation}}
    """.trimIndent()

    /**
     * 长记忆注入默认模板。仅作用于普通对话模式；ROLEPLAY 模式仍走 PromptContextAssembler 内的
     * "长期记忆 / 剧情约束 / 心境"三段系统模板，不受此处影响。
     */
    val INJECTION_PROMPT_TEMPLATE: String = """
        【已知信息（记忆）】
        以下信息是当前对话已经确认的稳定事实、偏好或约束，回答时不要与之冲突。
        {{memories}}
    """.trimIndent()
}
